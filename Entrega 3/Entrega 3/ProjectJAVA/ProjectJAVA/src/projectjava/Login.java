    private void btn_accederActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_accederActionPerformed
        user = txt_user.getText().trim();
        pass = txt_password.getText().trim();
        
        Login_class objLogin_class = new Login_class(user, pass);
        
        if(!user.equals("") || !pass.equals("")) {
        
            try {   
                Connection cn = Conexion.conectar();
                
                PreparedStatement ps = cn.prepareStatement("select tipo_nivel, Estatus from usuarios where Username ='" + user
                + "'and password = '" + pass + "'");
                
                
                ResultSet rs = ps.executeQuery();
                
                   if (rs.next()) {
                    String tipo_nivel = rs.getString("tipo_nivel");
                    String estatus = rs.getString("Estatus");
                    
                    if(tipo_nivel.equals("Administrador") && estatus.equals("Activo")) {
                    dispose();  
                    new Administrador().setVisible(true);
                    }else if(tipo_nivel.equals("Capturista") && estatus.equals("Activo")) {
                    new Capturista().setVisible(true);
                    }else if(tipo_nivel.equals("Desarrollador") && estatus.equals("Activo")) {
                    new Desarrollador().setVisible(true);
                    
                    }else {
                        JOptionPane.showMessageDialog(null, "ERROR al iniciar sesion");
                        
                    }
                }else {
                    JOptionPane.showMessageDialog(null, "No coinciden los datos.Ingrese los datos correctamente");
                    txt_user.setText("");
                    txt_password.setText("");
                }
            } catch (SQLException e) {
                System.err.println("Error en el boton de acceder" + e);
                
            }
        }else {
            JOptionPane.showInputDialog(null, "Debes llenar todos los campos, no debes dejar espacios vacios");
        
        }
        

    }//GEN-LAST:event_btn_accederActionPerformed

